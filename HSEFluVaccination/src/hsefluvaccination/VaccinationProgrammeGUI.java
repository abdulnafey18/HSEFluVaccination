    private void GroupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GroupButtonActionPerformed
        // TODO add your handling code here:
        if (!myPQueue.isEmpty()) {
            PQElement pqElement = (PQElement) myPQueue.dequeue();
            Person person = (Person) pqElement.getPerson();
            
            jTextArea1.append("""
                              
                              The person name is: """ + person.getsName() + "\n");
            jTextArea1.append("""
                              
                              Their Medical Condition is: """ + person.getsMedicalCondition() + "\n");
            jTextArea1.append("""
                              
                              Their Age is: """ + person.getsAge() + "\n");
            jTextArea1.append("""
                              
                              Their priority is: """ + pqElement.getiKey() + "\n");
            jTextArea1.append("""
                              
                              The registered person with priority: """ + pqElement.getiKey() + " has been Scheduled and Removed from the list.\n");
        } else
            jTextArea1.append("""
                              
                              There are no groups!
                              """);
    }//GEN-LAST:event_GroupButtonActionPerformed

