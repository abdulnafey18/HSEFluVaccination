    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        String sName;
        Boolean sMedicalCondition;
        int iPriority;
        Integer priorityINT, sAge;

        Person newPerson = new Person();
        sName = NameTextField.getText();
        sMedicalCondition = Boolean.valueOf(MCTextField.getText());
        sAge = Integer.parseInt(AgeTextField.getText());
        newPerson.setsName(sName);
        newPerson.setsMedicalCondition(sMedicalCondition);
        newPerson.setsAge(sAge);
        myPQueue.enqueue(sAge, newPerson, sMedicalCondition);

        jTextArea1.append(NameTextField.getText() + " has been successfully registered in the System\n");
        NameTextField.setText("");
        MCTextField.setText("");
        AgeTextField.setText("");
    }//GEN-LAST:event_AddButtonActionPerformed

